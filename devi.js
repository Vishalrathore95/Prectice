const card2 = document.getElementById("card2");

function getContainer(arr, container) {
    let newData = arr.map((item) => {
        const { name, imageSrc, time, type, isLiked, rating } = item;

        return `<div>
            <img src="${imageSrc}" alt="${name}">
            <h3>Recipe Name: ${name}</h3>
            <p> Time: ${time}</p>
            <p> Type: ${type}</p>
            <p> Like/Dislike: ${isLiked}</p>
            <p> Rating: ${rating}</p>
        </div>`;
    });

    container.innerHTML = newData.join('');

    if (arr.length <= 0) {
        return (container.innerHTML = `<h2>Item not found</h3>`);
    }
}

window.addEventListener('DOMContentLoaded', () => {
    getContainer(arr, card2);
});

// Sorting functions
function sortByVeg() {
    const veg = arr.filter(item => item.type.toLowerCase() === 'veg');
    getContainer(veg, card2);
}

function sortByNonVeg() {
    const nonVeg = arr.filter(item => item.type.toLowerCase() === 'non-veg');
    getContainer(nonVeg, card2);
}

// Search function
function Search() {
    const input = document.getElementById('gsearch').value.toLowerCase();
    const filteredRecipes = arr.filter(item =>
        item.type.toLowerCase().includes(input) ||
        item.name.toLowerCase().includes(input) ||
        item.time.toLowerCase().includes(input)
    );
    getContainer(filteredRecipes, card2);
}